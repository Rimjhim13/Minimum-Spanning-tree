    @Override
    public void paint(Graphics g){
        super.paint(g);
        g=jPanel1.getGraphics();
        Graphics2D g2d=(Graphics2D)g;
        g2d.setStroke(new BasicStroke(2));
        for(int i=0;i<node.size();i++){
            Ellipse2D.Double l1=(Ellipse2D.Double) node.get(i);
            double x=l1.getX()+20;
            double y=l1.getY()+20;
            g2d.draw(node.get(i));
            g2d.drawString(i+"", (int)x-2, (int)y-24);
            //g2d.draw(edge.get(i));
        }
        for(Shape e:edge){
            if(red[edge.indexOf(e)]){
                g2d.setColor(Color.red);
                g2d.draw(e);
            }
            else{
                g2d.setColor(Color.black);
                g2d.draw(e);
            }
            Line2D.Double l1=(Line2D.Double) e;
            double xx1=l1.x1;
            double xx2=l1.x2;
            double yy1=l1.y1;
            double yy2=l1.y2;
            g2d.drawString(edge.size()+"", (int)((xx1+xx2)/2)+10, (int)((yy1+yy2)/2)-10);
        }
    }
    
