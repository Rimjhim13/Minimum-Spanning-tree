    private void jPanel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseClicked
        // TODO add your handling code here:
        int x=evt.getX();
        int y=evt.getY();
        if(jToggleButton1.isSelected()){
        graphics=jPanel1.getGraphics();
        Graphics2D g2d=(Graphics2D) graphics;
        g2d.setStroke(new BasicStroke(2));
        graphics.setColor(Color.black);
        Shape cir=new Ellipse2D.Double(x-20,y-20,40,40);
        g2d.draw(cir);
        node.add(cir);
        g2d.drawString(node.size()-1+"", x-2, y-24);
        System.out.println(node.size()-1);
        }
        if(jToggleButton2.isSelected()){
            Graphics2D g2d=(Graphics2D)graphics;
            g2d.setStroke(new BasicStroke(2));
            graphics.setColor(Color.red); 
            for(Shape n: node){
                Ellipse2D.Double l1=(Ellipse2D.Double) n;
                double x1=l1.getX();
                double y1=l1.getY();
                double w1=l1.getWidth();
                double h1=l1.getHeight();
                if(n.contains(evt.getPoint())){
                    if(!select){
                        select=true;
                        x2=x1;y2=y1;h2=h1;w2=w1;
                    Shape newcir=new Ellipse2D.Double(x1, y1, w1, h1);
                    System.out.println(node.indexOf(n));
                    node.set(node.indexOf(n),newcir);
                    g2d.draw(newcir);
                    ed1.add(node.indexOf(n));
                    }
                    else{
                        select=false;
                        Ellipse2D.Double l2=(Ellipse2D.Double) n;
                        double x3=l1.getX();
                        double y3=l1.getY();
                        System.out.println(node.indexOf(n));
                        graphics.setColor(Color.black);
                        Shape cir2=new Ellipse2D.Double(x2, y2, w2, h2);
                        g2d.draw(cir2);
                        //node.set(node.indexOf(n), cir2);
                        lx1=x2+20;
                        ly1=y2+20;
                        lx2=x3+20;
                        ly2=y3+20;
                        Shape line=new Line2D.Double(lx1, ly1, lx2, ly2);
                        //drawArrowLine(g2d,(int)lx1, (int)ly1, (int)lx2, (int)ly2,5,5);
                        
                        g2d.draw(line);
                        edge.add(line);
                        g2d.drawString(edge.size()+"", (int)((lx1+lx2)/2)+10, (int)((ly1+ly2)/2)-10);
                        jComboBox1.addItem("edge-"+edge.size());
                        ed2.add(node.indexOf(n));
                    }                    
                }
            }
        }
    }//GEN-LAST:event_jPanel1MouseClicked

